#include "libft.h"

int		ft_atoi(const char *str)
{
	int	i;
	int	sign;
	int result;

	i = 0;
	result = 0;
	sign = 1;
	while (ft_iswhitespace(str[i]))
	{
		i++;
	}
	if (str[i] == '-' || str[i] == '+')
	{
		if (str[i] == '-')
			sign = -1;
		i++;
	}
	while (ft_isdigit(str[i]))
	{
		result = result * 10 + (str[i] - '0');
		i++;
	}
	return (result * sign);
}
